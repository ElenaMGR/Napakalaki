    public void setTreasure (Treasure t){
        treasureModel = t;
        jLNombreTreasure.setText(treasureModel.getName());
        jLTipoTreasure.setText(treasureModel.getType().toString());
        jLBonusTreasure.setText("Bonus: "+treasureModel.getBonus()+"");
        BufferedImage imagen=null;
        try {
            String ruta = "Treasures/"+treasureModel.getName()+".jpg";
            imagen = ImageIO.read(getClass().getResource(ruta));
        } catch (IOException ex) {
            Logger.getLogger(TreasureView.class.getName()).log(Level.SEVERE, null, ex);
        }
        jLImagen.setIcon((new ImageIcon(imagen)));
        
        repaint();
    }
    
    public boolean isSelected(){
        return selected;
    }
    
    public Treasure getTreasure(){
        return treasureModel;
    }

